package abc.sound;

/**
 * All possible Key Signatures are represented by this KeySignature class
 * @author joysyu
 *
 */
public enum KeySignature{
    //no sharps or flats
    C_MAJOR,
    A_MINOR,
    // 1 sharp
    G_MAJOR,
    E_MINOR,
    // 1 flat
    F_MAJOR,
    D_MINOR,
    // 2 sharps
    D_MAJOR,
    B_MINOR,
    // 2 flats
    B_FLAT_MAJOR,
    G_MINOR,
    // 3 sharps
    A_MAJOR,
    F_SHARP_MINOR,
    // 3 flats
    E_FLAT_MAJOR,
    C_MINOR,
    // 4 sharps
    E_MAJOR,
    C_SHARP_MINOR,
    // 4 flats
    A_FLAT_MAJOR,
    F_MINOR,
    // 5 sharps
    B_MAJOR,
    G_SHARP_MINOR,
    // 5 flats
    D_FLAT_MAJOR,
    B_FLAT_MINOR,
    // 6 sharps
    F_SHARP_MAJOR,
    D_SHARP_MINOR,
    // 6 flats
    G_FLAT_MAJOR,
    E_FLAT_MINOR,
    // 7 sharps
    C_SHARP_MAJOR,
    A_SHARP_MINOR,
    // 7 flats
    C_FLAT_MAJOR,
    A_FLAT_MINOR
    
}